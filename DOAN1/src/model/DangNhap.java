package model;

public class DangNhap {
	public String MaDangNhap;
	public String TenDangNhap;
	public String MatKhau;
	public Boolean LoaiTaiKhoan;
	public String getMaDangNhap() {
		return MaDangNhap;
	}
	public void setMaDangNhap(String maDangNhap) {
		MaDangNhap = maDangNhap;
	}
	public String getTenDangNhap() {
		return TenDangNhap;
	}
	public void setTenDangNhap(String tenDangNhap) {
		TenDangNhap = tenDangNhap;
	}
	public String getMatKhau() {
		return MatKhau;
	}
	public void setMatKhau(String matKhau) {
		MatKhau = matKhau;
	}
	public Boolean getLoaiTaiKhoan() {
		return LoaiTaiKhoan;
	}
	public void setLoaiTaiKhoan(Boolean loaiTaiKhoan) {
		LoaiTaiKhoan = loaiTaiKhoan;
	}

}
